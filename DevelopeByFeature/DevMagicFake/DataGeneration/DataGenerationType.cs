#region

using System;

#endregion

namespace M.Radwan.DevMagicFake.DataGeneration
{
    /// <summary>
    /// The data generation type.
    /// </summary>
    public class DataGenerationType
    {
        #region Properties

        /// <summary>
        /// Gets or sets Byte.
        /// </summary>
        public byte Byte { get; set; }

        /// <summary>
        /// Gets or sets Char.
        /// </summary>
        public char Char { get; set; }

        /// <summary>
        /// Gets or sets DateTime.
        /// </summary>
        public DateTime DateTime { get; set; }

        /// <summary>
        /// Gets or sets Decimal.
        /// </summary>
        public decimal Decimal { get; set; }

        /// <summary>
        /// Gets or sets Double.
        /// </summary>
        public double Double { get; set; }

        /// <summary>
        /// Gets or sets Float.
        /// </summary>
        public float Float { get; set; }

        /// <summary>
        /// Gets or sets Int.
        /// </summary>
        public int Int { get; set; }

        /// <summary>
        /// Gets or sets Long.
        /// </summary>
        public long Long { get; set; }

        /// <summary>
        /// Gets or sets Sbyte.
        /// </summary>
        public sbyte Sbyte { get; set; }

        /// <summary>
        /// Gets or sets Short.
        /// </summary>
        public short Short { get; set; }

        /// <summary>
        /// Gets or sets String.
        /// </summary>
        public string String { get; set; }

        /// <summary>
        /// Gets or sets UInt.
        /// </summary>
        public uint UInt { get; set; }

        /// <summary>
        /// Gets or sets ULong.
        /// </summary>
        public ulong ULong { get; set; }

        /// <summary>
        /// Gets or sets UShort.
        /// </summary>
        public ushort UShort { get; set; }

        #endregion
    }
}
